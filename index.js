let left1 = document.querySelector('.left1')
let right1 = document.querySelector('.right1')

let slider1 = document.querySelector('.slider1')

left1.addEventListener('click', leftSlider1)
right1.addEventListener('click', rightSlider1)

function leftSlider1() {
    slider1.style.transform="translateX(0)"
    slider1.style.transition="1.5s"
}

function rightSlider1() {
    slider1.style.transform="translateX(-1300px)"
    slider1.style.transition="1.5s"
}

